primo_numero= input("Dammi un numero! ")
secondo_numero= input("Dammi un secondo numero! ")
somma_numeri= int(primo_numero) + int(secondo_numero)
prodotto_numeri= int(primo_numero) ** int(secondo_numero)



print ("La somma di " + str(primo_numero) + " e " + str(secondo_numero) + " è uguale a " + str(somma_numeri))
print ("Il prodotto di " + str(primo_numero) + " e " + str(secondo_numero) + " è uguale a " + str(prodotto_numeri))
primo_numero = int(input("Dammi un numero: "))
secondo_numero = int(input("Dammi un secondo numero: "))
operazione_da_effettuare = input("Quale operazione vuoi effettuare tra * / - + ? ")

if operazione_da_effettuare == '+':
    print("Somma:", primo_numero + secondo_numero)
if operazione_da_effettuare == '*':
    print("Somma:", primo_numero * secondo_numero)
if operazione_da_effettuare == '/':
    print("Somma:", primo_numero / secondo_numero)
if operazione_da_effettuare == '-':
    print("Somma:", primo_numero - secondo_numero)
